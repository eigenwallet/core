//! Reading and writing the timestamps stored alongside swap states.
//!
//! Historically these timestamps were written in a format that does not zero-pad
//! the hour and uses a variable number of subsecond digits, so the stored strings
//! cannot be compared or ordered as text. They must always be parsed back into a
//! point in time first. The parser in this module accepts every variant ever
//! written, while new timestamps are written in a canonical fixed-width form that
//! is lossless, orders correctly even as plain text, and is still understood by
//! all existing readers.

use anyhow::{Context, Result};
use time::OffsetDateTime;
use time::format_description::BorrowedFormatItem;
use time::macros::format_description;

/// Lenient reader format covering every variant of `entered_at` ever written.
const LENIENT_FORMAT: &[BorrowedFormatItem<'_>] = format_description!(
    "[year]-[month]-[day] [hour padding:none]:[minute]:[second].[subsecond] \
     [offset_hour padding:none sign:mandatory]:[offset_minute]:[offset_second]"
);

/// Canonical fixed-width writer format: zero-padded hour, exactly 9 subsecond digits.
///
/// Unlike the legacy `Display` output this is lexicographically sortable, has a
/// stability guarantee (the `time` crate documents `Display` as unstable) and is
/// understood by SQLite's date functions after stripping the offset trailer.
const CANONICAL_FORMAT: &[BorrowedFormatItem<'_>] = format_description!(
    "[year]-[month]-[day] [hour]:[minute]:[second].[subsecond digits:9] \
     [offset_hour sign:mandatory]:[offset_minute]:[offset_second]"
);

/// Parse an `entered_at` value, whether written in the legacy or canonical format.
pub fn parse_entered_at(entered_at: &str) -> Result<OffsetDateTime> {
    OffsetDateTime::parse(entered_at, LENIENT_FORMAT)
        .with_context(|| format!("Failed to parse entered_at timestamp {entered_at:?}"))
}

/// Render a timestamp in the canonical `entered_at` format used for new rows.
pub fn format_entered_at(timestamp: OffsetDateTime) -> Result<String> {
    timestamp
        .format(CANONICAL_FORMAT)
        .context("Failed to format entered_at timestamp")
}

