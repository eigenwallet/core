    /// The core property: sorting `entered_at` strings of *mixed* generations by
    /// their parsed value yields chronological order of the underlying instants.
    #[test]
    fn sorting_by_parsed_value_is_chronological_across_mixed_formats() {
        let mut entries: Vec<(String, OffsetDateTime)> = CORPUS
            .iter()
            .enumerate()
            .map(|(i, c)| {
                let instant = dt(c);
                let rendered = if i % 2 == 0 {
                    instant.to_string()
                } else {
                    format_entered_at(instant).unwrap()
                };
                (rendered, instant)
            })
            .chain(RAW_LEGACY.iter().map(|(raw, c)| (raw.to_string(), dt(c))))
            .collect();

        // Deterministic Fisher-Yates shuffle (no `rand` dependency needed).
        let mut state: u64 = 0x9E37_79B9_7F4A_7C15;
        for i in (1..entries.len()).rev() {
            state = state
                .wrapping_mul(6_364_136_223_846_793_005)
                .wrapping_add(1_442_695_040_888_963_407);
            entries.swap(i, (state >> 33) as usize % (i + 1));
        }

        entries.sort_by_key(|(rendered, _)| parse_entered_at(rendered).unwrap());

        for window in entries.windows(2) {
            let (ref a_str, a) = window[0];
            let (ref b_str, b) = window[1];
            assert!(a <= b, "out of order: {a_str:?} sorted before {b_str:?}");
        }
    }

    /// Proves the corpus actually exercises the bug this module fixes: for legacy
    /// renderings, lexicographic order disagrees with chronological order.
    #[test]
    fn corpus_contains_lexicographic_chronological_disagreements() {
        let mut disagreements = 0;
        for a in CORPUS {
            for b in CORPUS {
                if (dt(a) < dt(b)) != (legacy(a) < legacy(b)) && dt(a) != dt(b) {
                    disagreements += 1;
                }
            }
        }
        assert!(disagreements > 0, "corpus has no lexicographic traps");
    }

    /// The canonical format is fixed-width, so its lexicographic order *does*
    /// match chronological order (useful for SQL-side `ORDER BY` on new rows).
    #[test]
    fn canonical_renderings_sort_lexicographically() {
        let mut instants: Vec<OffsetDateTime> = CORPUS.iter().map(dt).collect();
        instants.sort();

        let rendered: Vec<String> = instants
            .iter()
            .map(|instant| format_entered_at(*instant).unwrap())
            .collect();
        let mut sorted = rendered.clone();
        sorted.sort();

        assert_eq!(rendered, sorted);
    }
