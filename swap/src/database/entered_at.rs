    fn dt(c: &(i32, u8, u8, u8, u8, u8, u32)) -> OffsetDateTime {
        let &(year, month, day, hour, minute, second, nano) = c;
        let date = Date::from_calendar_date(year, Month::try_from(month).unwrap(), day)
            .unwrap_or_else(|e| panic!("invalid corpus date {c:?}: {e}"));
        let time = Time::from_hms_nano(hour, minute, second, nano)
            .unwrap_or_else(|e| panic!("invalid corpus time {c:?}: {e}"));
        PrimitiveDateTime::new(date, time).assume_utc()
    }

    /// Renders each corpus entry exactly the way production wrote it historically.
    fn legacy(c: &(i32, u8, u8, u8, u8, u8, u32)) -> String {
        dt(c).to_string()
    }

    /// Pins the `time` crate's `Display` output so an upgrade that changes it
    /// (and thereby the legacy assumptions in this module) fails loudly.
    #[test]
    fn legacy_display_format_is_what_we_assume() {
        assert_eq!(
            legacy(&(2024, 8, 19, 6, 11, 37, 475_038_000)),
            "2024-08-19 6:11:37.475038 +00:00:00"
        );
        assert_eq!(
            legacy(&(2026, 6, 9, 13, 5, 1, 0)),
            "2026-06-09 13:05:01.0 +00:00:00"
        );
        assert_eq!(
            legacy(&(2026, 6, 9, 9, 5, 1, 123_400_000)),
            "2026-06-09 9:05:01.1234 +00:00:00"
        );
    }

    #[test]
    fn parses_legacy_and_canonical_renderings_losslessly() {
        for c in CORPUS {
            let expected = dt(c);

            let legacy = legacy(c);
            assert_eq!(
                parse_entered_at(&legacy).unwrap(),
                expected,
                "legacy {legacy:?}"
            );

            let canonical = format_entered_at(expected).unwrap();
            assert_eq!(
                parse_entered_at(&canonical).unwrap(),
                expected,
                "canonical {canonical:?}"
            );
        }
    }

